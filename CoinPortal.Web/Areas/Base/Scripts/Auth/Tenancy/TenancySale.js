
jQuery(document).ready(function () {
    /*============================================================================================*/
    // Init
    /*============================================================================================*/

    
    /*============================================================================================*/

    /*============================================================================================*/
    // Events
    /*============================================================================================*/

    $('button.btnAddBasket').click(function () {
        var pckId = $(this).attr('pck-id');
        
        Sigma.ajax({
            url: "../../Base/TenancySale/AddPckToBasket",
            data: { pckId: pckId },
            onSuccess: function () {
                window.location = window.location.origin + "/Base/Order/Index";
            },
            showSuccessToast: false,
            showErrorToast: true
        });
    });


    /*============================================================================================*/
});

